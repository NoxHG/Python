nsem = int(input('Digite o dia da semana: '))

if nsem == 1:
    print('Domingo')
elif nsem == 2:
    print('Segunda-feira')
elif nsem == 3:
    print('Terça-feira')
elif nsem == 4:
    print('Quarta-feira')
elif nsem == 5:
    print('Quinta-feira')
elif nsem == 6:
    print('Sexta-feira')
elif nsem == 7:
    print('Sábado')
else:    
    print('Dia inválido')